    public void agregarEspecimen() {
        //int idPersona = Integer.parseInt(idPersona.getText());
        if (nombreCientificoText.getText().equals("") || etapaDeVidaText.getText().equals("") || sexoText.getText().equals("") || numeroIndividuosText.getText().equals("") || estadoEspecimentText.getText().equals("") || idEventoRecoleccionText.getText().equals("") || idMetodoText.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Todos los campos deben ser completados.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        //obtener la informacion de los campos de entrada
        String nombreCientifico = nombreCientificoText.getText();
        String etapaVida = etapaDeVidaText.getText();
        String sexo = sexoText.getText();
        int numeroIndividuos = Integer.parseInt(numeroIndividuosText.getText());
        String estado = estadoEspecimentText.getText(); 
        int idEvento = Integer.parseInt(idEventoRecoleccionText.getText());
        int idMetodo = Integer.parseInt(idMetodoText.getText());

        if (nombreCientifico.isEmpty() || idEvento <= 0 || idMetodo <= 0) {
            JOptionPane.showMessageDialog(null, "Campos vac√≠os o inv√°lidos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "CALL crear_especimen(?, ?, ?, ?, ?, ?, ?)"; // procedimiento para crear especimen

        try (Connection conexion = new ConexionBD().getConnection(); 
            //hacer consulta
             CallableStatement callableStatement = conexion.prepareCall(sql)) {
            
            //
            callableStatement.setString(1, nombreCientifico);      
            callableStatement.setString(2, etapaVida.isEmpty() ? null : etapaVida); 
            callableStatement.setString(3, sexo.isEmpty() ? null : sexo);          
            callableStatement.setInt(4, numeroIndividuos);            
            callableStatement.setString(5, estado);                 
            callableStatement.setInt(6, idEvento);                   
            callableStatement.setInt(7, idMetodo);                   

            callableStatement.execute();

            JOptionPane.showMessageDialog(null, "Especimen creado", "Exito", JOptionPane.INFORMATION_MESSAGE);

            nombreCientificoText.setText("");
            etapaDeVidaText.setText("");
            sexoText.setText("");
            numeroIndividuosText.setText("");
            estadoEspecimentText.setText("");
            idEventoRecoleccionText.setText("");
            idMetodoText.setText("");
            
            this.dispose();
            ModificarInsectos neframe = new ModificarInsectos(infoUsuarioActual);
            neframe.setVisible(true);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al crear el especimen: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error en el formato" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    


